import { Location } from '@angular/common';
import { Component, OnInit } from '@angular/core';
import { ActivatedRoute } from '@angular/router';
import { CoreService } from 'src/app/Services/CoreService/core.service';

@Component({
  selector: 'app-staticpage-detals',
  templateUrl: './staticpage-detals.component.html',
  styleUrls: ['./staticpage-detals.component.scss']
})
export class StaticpageDetalsComponent implements OnInit {

  constructor(private coreService: CoreService, private Arout: ActivatedRoute,private location:Location) { }

  slug: any
  details:any
  ngOnInit(): void {
    this.slug = this.Arout.snapshot.paramMap.get('slug');

    this.coreService.getStaticPageBySlug(this.slug).subscribe(res=>{
      // console.log(res);
      this.details=res;
      this.filteredData = this.details?.logs.slice(); // Initialize filteredData with the original data
      this.filterData(); 
    })
  }

  ngAfterViewInit() {
    this.jquery("assets/plugins/owlcarousel/owl.carousel.min.js")
    this.LoadScript("assets/js/product-details.js")
  }
  jquery(js: string) {
    var script = document.createElement('script');
    script.src = js;
    script.async = false;
    document.body.appendChild(script);
  }
  LoadScript(js: string) {
    var script = document.createElement('script');
    script.src = js;
    script.async = false;
    document.body.appendChild(script);
  }

  
  goBack() {
    this.location.back();
  }

  p: number = 1
  pageSize: number = 10;
  itemsPerPage = 10;
  key = 'id';
  reverse: boolean = false;
  sort(key) {
    this.key = key;
    this.reverse = !this.reverse
  }
  // filter data
  filteredData: any[]; 
  filterOpertion:any;
  filterData() {
    let filteredData = this.details?.logs.slice();
    if (this.filterOpertion) {
      filteredData = filteredData.filter((item) => item?.operation_type === this.filterOpertion);
    }
    this.filteredData = filteredData;
  }
  clearFilter() {
    this.filterOpertion=null;
    this.filterData();
  }

  changePg(val: any) {
    console.log(val);
    if (val == -1) {
      this.itemsPerPage = this.filteredData?.length;
    }
  }
}


